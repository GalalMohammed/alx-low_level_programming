#include "3-calc.h"

/**
  * op_add - computes sum of 2 int
  * @a: first int
  * @b: second int
  *
  * Return: a + b
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - computes sub of 2 int
  * @a: first int
  * @b: second int
  *
  * Return: a - b
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - computes mul of 2 int
  * @a: first int
  * @b: second int
  *
  * Return: a * b
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - computes div of 2 int
  * @a: first int
  * @b: second int
  *
  * Return: a / b
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - computes mod of 2 int
  * @a: first int
  * @b: second int
  *
  * Return: a % b
  */
int op_mod(int a, int b)
{
	return (a % b);
}
